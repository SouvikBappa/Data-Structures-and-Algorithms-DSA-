#include<iostream>
using namespace std;

int main(){
    //Bitwise Operator
    cout<<(3 & 5)<<endl;
    cout<<(3 | 5)<<endl;
    cout<<(3 ^ 5)<<endl;
    cout<<(~6)<<endl;
    cout<<(~0)<<endl;
    cout<<(7<<2)<<endl;
    cout<<(7>>2)<<endl;
    cout<<(~4)<<endl;
    cout<<(8>>1)<<endl;
    return 0;
}